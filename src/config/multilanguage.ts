export enum Language {
    VI = 'vi',
    TH = 'th',
    EN = 'en',
}

export enum Timezone {
    VN = 'Asia/Ho_Chi_Minh',
    TH = 'Asia/Ho_Chi_Minh',
    UTC = 'UTC',
}

export function getDefaultTimeZoneByCountry(country: Country): string {
    const pickedCountry = ct.getCountry(country);
    return pickedCountry.timezones[0];
}

export const DefaultLanguageByCountry = {
    [Country.VIETNAM]: Language.VI,
    [Country.THAILAND]: Language.TH,
};
export function getMatchedLanguageFromUserLanguage(lang: string): Language {
    if (lang === Language.VI) {
        return Language.VI;
    }
    if (lang === Language.TH) {
        return Language.TH;
    }
    return Language.EN;
}
